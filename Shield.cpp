void Shield::draw() const {
    for (int row = 0; row < grid.size(); row++) {
        for (int col = 0; col < grid[row].size(); col++) {
            if (grid[row][col] == 1) {
                DrawRectangle(
                    static_cast<int>(position.x + col * blockSize),
                    static_cast<int>(position.y + row * blockSize),
                    static_cast<int>(blockSize),
                    static_cast<int>(blockSize),
                    GREEN
                );
            }
        }
    }
}

void Shield::takeDamage(int hitX, int hitY, int damageRadius) {
    int coordinateX = (hitX - static_cast<int>(position.x)) / blockSize;
    int coordinateY = (hitY - static_cast<int>(position.y)) / blockSize;

    for (int row = -damageRadius; row <= damageRadius; row++) {
        for (int col = -damageRadius; col <= damageRadius; col++) {
            int areaX = coordinateX + col;
            int areaY = coordinateY + row;
            if (areaX >= 0 && areaX < grid[0].size() && areaY >= 0 && areaY < grid.size()) {
                if (row * row + col * col <= damageRadius * damageRadius) {
                    grid[areaY][areaX] = 0;
                }
            }
        }
    }
}

const std::vector<std::vector<int>>& Shield::getGrid() const {
    return grid;
}

Vector2 Shield::getPosition() const {
    return position;
}

float Shield::getBlockSize() const {
    return blockSize;
}

float Shield::getWidth() const {
    return grid[0].size() * blockSize;
}

float Shield::getHeight() const {
    return grid.size() * blockSize;
}